
public class Batter
	{
		String Name;
		double Average;
		Batter(String n, double a)
		{
		Name = n;
		Average = a;
		}
		public String getName()
			{
				return Name;
			}
		public void setName(String name)
			{
				Name = name;
			}
		public double getAverage()
			{
				return Average;
			}
		public void setAverage(double average)
			{
				Average = average;
			}
	}

		